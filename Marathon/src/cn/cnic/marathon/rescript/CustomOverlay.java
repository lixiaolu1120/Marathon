package cn.cnic.marathon.rescript;

import com.baidu.mapapi.map.OverlayOptions;

public class CustomOverlay {
	String type;
	OverlayOptions options;

	public CustomOverlay(String type, OverlayOptions options) {
		super();
		this.type = type;
		this.options = options;
	}

	public OverlayOptions getOptions() {
		return options;
	}

	public String getType() {
		return type;
	}
}
